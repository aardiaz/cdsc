package aggregation;

public class Car {
	
	private String color;
	private String model;
	private int price;
	
	public Car(String color, String model, int price) {
		this.color = color;
		this.model = model;
		this.price = price;
	}

	public String getColor() {
		return color;
	}

	public String getModel() {
		return model;
	}

	public int getPrice() {
		return price;
	}
	

}
